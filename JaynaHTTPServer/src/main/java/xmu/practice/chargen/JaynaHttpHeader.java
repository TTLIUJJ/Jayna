package xmu.practice.chargen;

public class JaynaHttpHeader {
    private int keyStart;
    private int keyEnd;
    private int valueStart;
    private int valueEnd;

    public int getKeyStart() {
        return keyStart;
    }

    public void setKeyStart(int keyStart) {
        this.keyStart = keyStart;
    }

    public int getKeyEnd() {
        return keyEnd;
    }

    public void setKeyEnd(int keyEnd) {
        this.keyEnd = keyEnd;
    }

    public int getValueStart() {
        return valueStart;
    }

    public void setValueStart(int valueStart) {
        this.valueStart = valueStart;
    }

    public int getValueEnd() {
        return valueEnd;
    }

    public void setValueEnd(int valueEnd) {
        this.valueEnd = valueEnd;
    }
}
